import type { Agent, AgentCategory, Plan } from './types'

// ── Helper to build an Agent from compact data ──────────────
function mkAgent(
  id: string, slug: string, name: string, category: AgentCategory,
  tier: 'free' | 'pro' | 'enterprise', icon: string, description: string,
  capabilities: string[], rating: number, deployCount: number,
  longDescription?: string,
): Agent {
  const tierMap = { free: 'FREE' as const, pro: 'PRO' as const, enterprise: 'ENTERPRISE' as const }
  const priceMap = { FREE: { monthly: 0, annual: 0 }, PRO: { monthly: 2900, annual: 27900 }, ENTERPRISE: { monthly: 9900, annual: 95900 } }
  const mapped = tierMap[tier]
  return {
    id, slug, name, category, tier: mapped, description, longDescription, icon, capabilities, rating, deployCount,
    short_name: name,
    tagline: description,
    active: true,
    monthly_price: priceMap[mapped].monthly,
    annual_price: priceMap[mapped].annual,
    one_time_price: 0,
    review_count: Math.floor(deployCount * 0.3),
    usage_count: deployCount,
    featured: rating >= 4.8,
    demo_available: true,
    created_at: '2026-01-01T00:00:00Z',
    branch: category,
    suite: category,
  }
}

